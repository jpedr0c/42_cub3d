# include "../include/cub3d.h"

int	create_rgba(int r, int g, int b, int a)
{
    if (r < 0 || r > 256 || g < 0 || g > 256 || b < 0 || b > 256
        || a < 0 || a > 256)
        return (0);
	return (r << 24 | g << 16 | b << 8 | a);
}